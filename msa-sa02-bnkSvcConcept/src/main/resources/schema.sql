--  DDL for Function UF_COUNT_AREA
--------------------------------------------------------

  CREATE OR REPLACE NONEDITIONABLE FUNCTION "CONCEPT"."UF_COUNT_AREA" (PV_AREA IN VARCHAR2)
RETURN NUMBER
--지역을 인수로 받아 해당하는 부서를 찾고, 그 부서에 소속된 사원 수를 구하여
--최종적으로 지역에 해당되는 사원 수를 구하는 함수
IS
V_DEPT_CNT NUMBER(10); --> 부서의 사원 수
V_TOTAL_CNT NUMBER(10); --> 지역의 사원 수

--지역에 해당되는 부서를 찾는 커서
CURSOR DEPT_CURSOR
IS
SELECT 부서코드
FROM 부서
WHERE 지역 = PV_AREA;

BEGIN
  V_TOTAL_CNT := 0;
  --부서별로 사원 수를 구함
  FOR RES IN DEPT_CURSOR
  LOOP
    SELECT COUNT(*)
    INTO V_DEPT_CNT
    FROM 사원 A
    WHERE A.부서코드 = RES.부서코드
    AND A.퇴사일자 IS NULL;
    
    V_TOTAL_CNT := V_TOTAL_CNT + V_DEPT_CNT;
  END LOOP;
  
  RETURN V_TOTAL_CNT;
END; 

